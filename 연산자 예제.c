#include <stdio.h>

int main()
{
	int num1 = 9, num2 = 1;
	printf(("%d + %d = %d \n"), num1, num2, num1 + num2);
	printf(("%d - %d = %d \n"), num1, num2, num1 - num2);
	printf(("%d * %d = %d \n"), num1, num2, num1 * num2);
	printf(("%d / %d = %d \n"), num1, num2, num1 / num2);
	return 0;
}
